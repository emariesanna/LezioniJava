public class Cliente {
    public String nome;
    public String cognome;
    public int telefono;
    public int numero_accesso;

    Cliente(String nome, String cognome, int telefono, int numero_accesso) {
        this.nome = nome;
        this.cognome = cognome;
        this.telefono = telefono;
        this.numero_accesso = numero_accesso;
    }
}
